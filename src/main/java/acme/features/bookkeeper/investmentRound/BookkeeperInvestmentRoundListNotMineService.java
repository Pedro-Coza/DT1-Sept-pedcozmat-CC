
package acme.features.bookkeeper.investmentRound;

import java.util.ArrayList;
import java.util.Collection;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import acme.entities.investmentRound.InvestmentRound;
import acme.entities.roles.Bookkeeper;
import acme.framework.components.Model;
import acme.framework.components.Request;
import acme.framework.services.AbstractListService;

@Service
public class BookkeeperInvestmentRoundListNotMineService implements AbstractListService<Bookkeeper, InvestmentRound> {

	@Autowired
	BookkeeperInvestmentRoundRepository repository;


	@Override
	public boolean authorise(final Request<InvestmentRound> request) {
		assert request != null;
		return true;
	}

	@Override
	public void unbind(final Request<InvestmentRound> request, final InvestmentRound entity, final Model model) {
		assert request != null;
		assert entity != null;
		assert model != null;

		request.unbind(entity, model, "ticker", "kindRound", "title", "money", "info");
	}

	@Override
	public Collection<InvestmentRound> findMany(final Request<InvestmentRound> request) {
		assert request != null;

		Collection<InvestmentRound> result = new ArrayList<InvestmentRound>();
		Collection<Integer> allIds;
		Collection<Integer> ids;

		allIds = this.repository.getInvRoundIds();
		ids = this.repository.findManyByBookKId(request.getPrincipal().getActiveRoleId());
		allIds.removeAll(ids);

		for (Integer i : allIds) {
			result.add(this.repository.findOneById(i));
		}

		return result;
	}
}
